# ---------------------------------------------------------------------------
# Variable rewriter for Group / Gizmo content
# ---------------------------------------------------------------------------

def _rewrite_group_vars(content: str, node_name: str, counter: typing.List[int]) -> str:
    """
    Rewrite internal set/push variable names inside a Group/Gizmo content
    blob to fresh unique names.

    WHY: Group nodes in Format B contain internal `set N5757400 [stack 0]`
    and `push $N5757400` directives where the variable names are memory
    addresses from the original Nuke session.  When exported to a temp file,
    any `push $VAR` that references a variable defined OUTSIDE this Group
    would be undefined, causing Nuke to push nothing and break all internal
    connections.

    This function renames every `set`-defined variable to a fresh unique
    name (`GV_{node}_{n}`) and updates all matching `push $` references.
    Variables that are pushed but never set in this block (external inputs)
    are left unchanged — the outer write-stack handles those.
    """
    set_re = re.compile(r"(^set\s+)(\w+)(\s+\[stack\s+\d+\])", re.MULTILINE)
    old_to_new: typing.Dict[str, str] = {}

    def _replace_set(m: re.Match) -> str:  # type: ignore[type-arg]
        old = m.group(2)
        if old not in old_to_new:
            safe = re.sub(r"\W", "_", node_name)
            old_to_new[old] = f"GV_{safe}_{counter[0]}"
            counter[0] += 1
        return m.group(1) + old_to_new[old] + m.group(3)

    content = set_re.sub(_replace_set, content)
    for old, new in old_to_new.items():
        content = re.sub(r"(push\s+\$)" + re.escape(old) + r"\b", r"\g<1>" + new, content)
    return content


