class EdgeItem(QtWidgets.QGraphicsPathItem):
    """
    Connection line from a parent node's output (bottom-centre) to a child
    node's specific input slot (top edge), matching Nuke's wiring convention.

    Slot positions along the child's top edge:
      - 1 input  → centre
      - 2 inputs → left-third, right-third  (matching Nuke's A/B layout)
      - N inputs → evenly distributed across the top edge
    """

    _PEN = QtGui.QPen(QtGui.QColor(160, 160, 160), 1.2, QtCore.Qt.SolidLine)

    def __init__(
        self,
        parent_item: "NodeItem",
        child_item:  "NodeItem",
        slot:        int = 0,
        num_slots:   int = 1,
    ) -> None:
        super().__init__()
        self.parent_item = parent_item
        self.child_item  = child_item
        self._slot      = slot
        self._num_slots = max(num_slots, 1)
        self.setFlag(QtWidgets.QGraphicsItem.ItemIsSelectable, False)
        self.setZValue(-1)
        self.setPen(self._PEN)
        self._update_path()

    def _slot_x(self, rect: QtCore.QRectF, slot: int, num_slots: int) -> float:
        """Return the scene X coordinate for a given slot along a node's top edge."""
        if num_slots <= 1:
            return rect.center().x()
        # Distribute evenly: slot 0 at left edge + half-division width,
        # last slot at right edge - half-division width
        division = rect.width() / num_slots
        return rect.left() + division * slot + division * 0.5

    def _update_path(self) -> None:
        pr = self.parent_item.sceneBoundingRect()
        cr = self.child_item.sceneBoundingRect()

        # Output: bottom-centre of parent (DotItem uses centre)
        if isinstance(self.parent_item, DotItem):
            src = pr.center()
        else:
            src = QtCore.QPointF(pr.center().x(), pr.bottom())

        # Input: correct slot position on child's top edge (DotItem uses centre)
        if isinstance(self.child_item, DotItem):
            dst = cr.center()
        else:
            dst = QtCore.QPointF(
                self._slot_x(cr, self._slot, self._num_slots),
                cr.top(),
            )

        path = QtGui.QPainterPath(src)
        path.lineTo(dst)
        self.setPath(path)




