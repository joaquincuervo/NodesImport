class BackdropItem(QtWidgets.QGraphicsRectItem):
    """
    Semi-transparent selectable backdrop rectangle.
    Sized from bdwidth/bdheight knobs. Fill is tile_color at 20% alpha.
    Sits at zValue -2 (behind edges and nodes).
    Backdrops can be selected and imported just like regular nodes.
    """

    def __init__(self, node_data: typing.Any) -> None:
        super().__init__()
        self.node_data = node_data

        w_m  = re.search(r"^\s*bdwidth\s+(\d+)",        node_data.content, re.MULTILINE)
        h_m  = re.search(r"^\s*bdheight\s+(\d+)",       node_data.content, re.MULTILINE)
        l_m  = re.search(r"^\s*label\s+(.+)",           node_data.content, re.MULTILINE)
        fs_m = re.search(r"^\s*note_font_size\s+(\d+)", node_data.content, re.MULTILINE)

        width  = int(w_m.group(1))  if w_m  else 200
        height = int(h_m.group(1))  if h_m  else 100
        raw_label = l_m.group(1).strip().strip('"').strip("'") if l_m else ""
        # Strip HTML tags, then unescape \n and \t escape sequences
        label = re.sub(r"<[^>]+>", "", raw_label).strip()
        label = label.replace("\\n", "\n").replace("\\t", "\t")
        font_size = min(int(fs_m.group(1)), 18) if fs_m else 14

        self.setRect(0, 0, width, height)
        self.setPos(node_data.xpos, node_data.ypos)
        # Backdrops are NOT natively selectable by Qt — we manage their
        # selection state manually in GraphView.mouseReleaseEvent so we can
        # enforce full-containment semantics (a backdrop is only selected when
        # the rubber-band completely encloses it, never on a partial touch or
        # single click).  ItemIsSelectable is deliberately omitted so Qt's
        # rubber-band and click handlers never select a backdrop on their own.
        # ItemIsSelectable=True lets setSelected() work so our import logic
        # can mark backdrops as selected. But we set AcceptedMouseButtons to
        # NoButton so Qt's own click/rubber-band handler never selects a backdrop
        # directly — we manage that entirely in GraphView.mouseReleaseEvent with
        # the full-containment rule.
        self.setFlag(QtWidgets.QGraphicsItem.ItemIsSelectable)
        self.setFlag(QtWidgets.QGraphicsItem.ItemIsMovable, False)
        self.setZValue(-2)
        self.setAcceptedMouseButtons(QtCore.Qt.NoButton)

        self._base = self._base_color()
        fill   = QtGui.QColor(self._base); fill.setAlpha(51)    # 20%
        border = QtGui.QColor(self._base); border.setAlpha(153) # 60%
        self.setBrush(QtGui.QBrush(fill))
        self._normal_pen         = QtGui.QPen(border, 2)
        self._selected_pen       = QtGui.QPen(QtGui.QColor(255, 210, 0), 3)
        self._deselect_prev_pen  = QtGui.QPen(QtGui.QColor(200, 120, 30), 3)
        self.setPen(self._normal_pen)
        self._deselect_preview: bool = False

        if label:
            self.label = QtWidgets.QGraphicsTextItem(label, self)
            self.label.setDefaultTextColor(QtGui.QColor(255, 255, 255, 200))
            font = self.label.font()
            font.setPointSize(font_size)
            font.setBold(True)
            self.label.setFont(font)
            self.label.setPos(8, 4)
            self.label.setTextWidth(float(width - 16))
            # Label must not intercept mouse events — clicks should pass
            # through to the view so nodes inside the backdrop are reachable.
            self.label.setFlag(QtWidgets.QGraphicsItem.ItemIsSelectable, False)
            self.label.setAcceptedMouseButtons(QtCore.Qt.NoButton)
            self.label.setAcceptHoverEvents(False)

    def _base_color(self) -> QtGui.QColor:
        if (
            hasattr(self.node_data, "tile_color")
            and self.node_data.tile_color is not None
        ):
            c = self.node_data.tile_color
            r, g, b, a = (c >> 24) & 0xFF, (c >> 16) & 0xFF, (c >> 8) & 0xFF, c & 0xFF
            if a > 0:
                return QtGui.QColor(r, g, b)
        return QtGui.QColor(80, 80, 80)

    def paint(
        self,
        painter: QtGui.QPainter,
        option:  QtWidgets.QStyleOptionGraphicsItem,
        widget:  typing.Optional[QtWidgets.QWidget] = None,
    ) -> None:
        option.state &= ~QtWidgets.QStyle.State_Selected  # type: ignore[operator]
        super().paint(painter, option, widget)
        if self._deselect_preview:
            painter.setPen(self._deselect_prev_pen)
            painter.setBrush(QtCore.Qt.NoBrush)
            painter.drawRect(self.rect())
        elif self.isSelected():
            painter.setPen(self._selected_pen)
            painter.setBrush(QtCore.Qt.NoBrush)
            painter.drawRect(self.rect())


