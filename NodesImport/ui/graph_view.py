class NodeItem(QtWidgets.QGraphicsRectItem):
    def __init__(self, node_data: typing.Any) -> None:
        super().__init__()
        self.node_data = node_data

        is_group = node_data.node_type.lower() in _FLAT_GROUP_TYPES
        width  = 100 if is_group else 80
        height =  40 if is_group else 30
        self.setRect(0, 0, width, height)

        self.setFlag(QtWidgets.QGraphicsItem.ItemIsSelectable)
        self.setFlag(QtWidgets.QGraphicsItem.ItemIsMovable)

        color = self._get_color()
        self.setBrush(QtGui.QBrush(color))
        self.setPen(QtGui.QPen(QtGui.QColor(100, 100, 100), 2 if is_group else 1))

        self.label = QtWidgets.QGraphicsTextItem(node_data.name, self)
        tc = QtGui.QColor(255, 255, 255) if color.lightness() < 128 else QtGui.QColor(0, 0, 0)
        self.label.setDefaultTextColor(tc)
        font = self.label.font()
        font.setPointSize(7)
        font.setBold(is_group)
        self.label.setFont(font)
        lw = min(self.label.boundingRect().width(), float(width))
        lh = self.label.boundingRect().height()
        self.label.setPos((width - lw) / 2.0, (height - lh) / 2.0)
        self.label.setTextWidth(float(width))

        self.setPos(node_data.xpos, node_data.ypos)
        self._deselect_preview: bool = False  # True while inside a Ctrl+Drag band

    def _get_color(self) -> QtGui.QColor:
        if (
            hasattr(self.node_data, "tile_color")
            and self.node_data.tile_color is not None
        ):
            c = self.node_data.tile_color
            r, g, b, a = (c >> 24) & 0xFF, (c >> 16) & 0xFF, (c >> 8) & 0xFF, c & 0xFF
            if a > 0:
                return QtGui.QColor(r, g, b, a)

        nt = self.node_data.node_type.lower()
        for keywords, color in _COLOR_MAP:
            if any(kw in nt for kw in keywords):
                return color
        return QtGui.QColor(60, 60, 60)

    def paint(
        self,
        painter: QtGui.QPainter,
        option:  QtWidgets.QStyleOptionGraphicsItem,
        widget:  typing.Optional[QtWidgets.QWidget] = None,
    ) -> None:
        option.state &= ~QtWidgets.QStyle.State_Selected  # type: ignore[operator]
        super().paint(painter, option, widget)
        if self._deselect_preview:
            # Dim orange ring: "this node will be deselected on release"
            painter.setPen(QtGui.QPen(QtGui.QColor(200, 120, 30), 2))
            painter.setBrush(QtCore.Qt.NoBrush)
            painter.drawRect(self.rect())
        elif self.isSelected():
            painter.setPen(QtGui.QPen(QtGui.QColor(255, 210, 0), 2))
            painter.setBrush(QtCore.Qt.NoBrush)
            painter.drawRect(self.rect())


