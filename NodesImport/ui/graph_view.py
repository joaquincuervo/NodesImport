class DotItem(QtWidgets.QGraphicsEllipseItem):
    """
    Dot nodes are rendered as small filled circles (12×12 px), matching
    Nuke's own DAG style. No label is shown. The circle is centred on the
    node's (xpos, ypos) coordinate so edges connect naturally.
    """
    _SIZE = 12

    def __init__(self, node_data: typing.Any) -> None:
        r = self._SIZE / 2
        super().__init__(-r, -r, self._SIZE, self._SIZE)
        self.node_data = node_data
        # In Nuke's .nk format, Dot xpos/ypos is the CENTRE of the dot
        # (unlike regular nodes where it is the top-left of the rect).
        self.setPos(node_data.xpos, node_data.ypos)
        self.setFlag(QtWidgets.QGraphicsItem.ItemIsSelectable)
        self.setFlag(QtWidgets.QGraphicsItem.ItemIsMovable)
        self.setZValue(0)
        self.setBrush(QtGui.QBrush(QtGui.QColor(90, 90, 90)))
        self.setPen(QtGui.QPen(QtGui.QColor(140, 140, 140), 1.5))
        self._deselect_preview: bool = False

    def paint(
        self,
        painter: QtGui.QPainter,
        option:  QtWidgets.QStyleOptionGraphicsItem,
        widget:  typing.Optional[QtWidgets.QWidget] = None,
    ) -> None:
        option.state &= ~QtWidgets.QStyle.State_Selected  # type: ignore[operator]
        super().paint(painter, option, widget)
        r = self._SIZE / 2 + 2
        if self._deselect_preview:
            painter.setPen(QtGui.QPen(QtGui.QColor(200, 120, 30), 2))
            painter.setBrush(QtCore.Qt.NoBrush)
            painter.drawEllipse(QtCore.QRectF(-r, -r, r * 2, r * 2))
        elif self.isSelected():
            painter.setPen(QtGui.QPen(QtGui.QColor(255, 210, 0), 2))
            painter.setBrush(QtCore.Qt.NoBrush)
            painter.drawEllipse(QtCore.QRectF(-r, -r, r * 2, r * 2))

